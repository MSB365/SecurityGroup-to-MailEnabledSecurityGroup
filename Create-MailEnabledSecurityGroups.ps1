        [int]$SuccessfulGroups,
        [int]$FailedGroups,
        [int]$TotalMembersAdded,
        [string]$EmailDomain,
        [string]$GroupPrefix
    )
    
    $Duration = $EndTime - $StartTime
    $ResultsHtml = ""
    $SkippedMembersHtml = ""
    
